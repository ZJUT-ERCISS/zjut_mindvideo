def linear_assignment(cost_matrix, thresh):
    """linear assignment"""
    if cost_matrix.size == 0:
        return np.empty((0, 2), dtype=int), tuple(range(cost_matrix.shape[0])), tuple(range(cost_matrix.shape[1]))
    matches, unmatched_a, unmatched_b = [], [], []
    _, x, y = lap.lapjv(cost_matrix, extend_cost=True, cost_limit=thresh)
    for ix, mx in enumerate(x):
        if mx >= 0:
            matches.append([ix, mx])
    unmatched_a = np.where(x < 0)[0]
    unmatched_b = np.where(y < 0)[0]
    matches = np.asarray(matches)
    return matches, unmatched_a, unmatched_b


def ious(atlbrs, btlbrs):
    """
    Compute cost based on IoU.

    Args:
        atlbrs (list[tlbr], numpy.ndarray): Coordinate and size of first bboxs.
        btlbrs (list[tlbr], numpy.ndarray): Coordinate and size of second bboxs.

    Returns:
        numpy.ndarray, ious.

    """
    iou = np.zeros((len(atlbrs), len(btlbrs)), dtype=np.float)
    if iou.size == 0:
        return iou

    iou = bbox_ious(
        np.ascontiguousarray(atlbrs, dtype=np.float),
        np.ascontiguousarray(btlbrs, dtype=np.float)
    )

    return iou


def iou_distance(atracks, btracks):
    """
    Compute cost based on IoU

    Args:
        atlbrs (list[Track]): Coordinate and size of first tracking bboxs.
        btlbrs (list[Track]): Coordinate and size of second tracking bboxs.

    Returns:
        numpy.ndarray, cost_matrix.
    """

    if (atracks and isinstance(atracks[0], np.ndarray)) or (
            btracks and isinstance(btracks[0], np.ndarray)):
        atlbrs = atracks
        btlbrs = btracks
    else:
        atlbrs = [track.tlbr for track in atracks]
        btlbrs = [track.tlbr for track in btracks]
    iou_matrix = ious(atlbrs, btlbrs)
    cost_matrix = 1 - iou_matrix

    return cost_matrix


def embedding_distance(tracks, detections, metric='cosine'):
    """
    Embedding distance.

    Args:
        tracks: (list[Track]):
        detections (list[BaseTrack]):
        metric (str): Type of metric, please refer to `scipy.spatial.distance.cdist`.
            Default: 'cosine'
    Returns:
        numpy.ndarray, cost_matrix.
    """

    cost_matrix = np.zeros((len(tracks), len(detections)), dtype=np.float)
    if cost_matrix.size == 0:
        return cost_matrix
    det_features = np.asarray([track.curr_feat for track in detections], dtype=np.float)
    # for i, track in enumerate(tracks):
    # cost_matrix[i, :] = np.maximum(0.0, cdist(track.smooth_feat.reshape(1,-1), det_features, metric))
    track_features = np.asarray([track.smooth_feature for track in tracks], dtype=np.float)
    cost_matrix = np.maximum(0.0, cdist(track_features, det_features, metric))  # Nomalized features
    return cost_matrix


def gate_cost_matrix(kf, cost_matrix, tracks, detections, only_position=False):
    """gate cost matrix"""
    if cost_matrix.size == 0:
        return cost_matrix
    gating_dim = 2 if only_position else 4
    gating_threshold = kalman_filter.chi2inv95[gating_dim]
    measurements = np.asarray([det.to_xyah() for det in detections])
    for row, track in enumerate(tracks):
        gating_distance = kf.gating_distance(
            track.mean, track.cov, measurements, only_position)
        cost_matrix[row, gating_distance > gating_threshold] = np.inf
    return cost_matrix


def fuse_motion(kf, cost_matrix, tracks, detections, only_position=False, lambda_=0.98):
    """fuse motion"""
    if cost_matrix.size == 0:
        return cost_matrix
    gating_dim = 2 if only_position else 4
    gating_threshold = kalman_filter.chi2inv95[gating_dim]
    measurements = np.asarray([det.to_xyah() for det in detections])
    for row, track in enumerate(tracks):
        gating_distance = kf.gating_distance(
            track.mean, track.cov, measurements, only_position, metric='maha')
        cost_matrix[row, gating_distance > gating_threshold] = np.inf
        cost_matrix[row] = lambda_ * cost_matrix[row] + (1 - lambda_) * gating_distance
    return cost_matrix
